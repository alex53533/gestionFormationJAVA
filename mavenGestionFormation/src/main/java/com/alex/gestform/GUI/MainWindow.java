    private void btnEnvoiFormationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEnvoiFormationMouseClicked
              
        try{
            DateFormat df = new SimpleDateFormat("MM/dd/yyyy");
            String datedeb = dateDebutFormation.getText();
            Date dateDebut = df.parse(datedeb);    
            
            java.sql.Date dtSQL = new java.sql.Date(dateDebut.getTime());

            String libelle = libelleFormation.getText();
            String description = descriptionFormation.getText();
            String lieu = lieuFormation.getText();
            String rem = nbHeuresFormation.getText();
            int nbHeures = Integer.parseInt(rem);

           

            Formation f = new Formation(libelle, description, lieu, nbHeures, dtSQL);

            FormationDAO.save(f);
        }
        catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEnvoiFormationMouseClicked

