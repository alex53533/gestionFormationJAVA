     private void loadRecords() throws SQLException {

        String sql_stmt = "SELECT * FROM stagiaire;";

        ResultSetTableModel tableModel = new ResultSetTableModel(sql_stmt);

        jTableStagiaires.setModel(tableModel);

        jTableStagiaires.getSelectionModel().addListSelectionListener((ListSelectionEvent event) -> {
            try {
                if (jTableStagiaires.getSelectedRow() >= 0) {
                    Object ocpStagiaire = jTableStagiaires.getValueAt(jTableStagiaires.getSelectedRow(), 0);
                    Object otelStagiaire = jTableStagiaires.getValueAt(jTableStagiaires.getSelectedRow(), 1);
                    Object onomStagiaire = jTableStagiaires.getValueAt(jTableStagiaires.getSelectedRow(), 2);
                    Object oprenomStagiaire = jTableStagiaires.getValueAt(jTableStagiaires.getSelectedRow(), 3);
                    Object oadresseStagiaire = jTableStagiaires.getValueAt(jTableStagiaires.getSelectedRow(), 4);
                    Object ovilleStagiaire = jTableStagiaires.getValueAt(jTableStagiaires.getSelectedRow(), 5);
                    Object oemailStagiaire = jTableStagiaires.getValueAt(jTableStagiaires.getSelectedRow(), 6);

                    cpStagiaire.setText(ocpStagiaire.toString());
                    telStagiaire.setText(otelStagiaire.toString());
                    nomStagiaire.setText(onomStagiaire.toString());
                    prenomStagiaire.setText(oprenomStagiaire.toString());
                    adresseStagiaire.setText(oadresseStagiaire.toString());
                    villeStagiaire.setText(ovilleStagiaire.toString());
                    emailStagiaire.setText(oemailStagiaire.toString());
                }
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            }
        });

        DefaultTableCellRenderer rightRenderer = new DefaultTableCellRenderer();
        rightRenderer.setHorizontalAlignment(SwingConstants.LEFT);
        jTableStagiaires.getColumnModel().getColumn(0).setCellRenderer(rightRenderer);
    }
    
    
    
    
